package org.talend.commons.exception;

/**
 * PersistenceException occuring when a resource cannot be found.<br/>
 * 
 * Not founded resource could be a folder, a project or any object.<br/>
 * 
 * $Id$
 * 
 */
public class ResourceNotFoundException extends PersistenceException {

    private static final long serialVersionUID = 3556433676899745391L;

    public ResourceNotFoundException(String message, Throwable cause) {
        super(message, cause);
    }

    public ResourceNotFoundException(String message) {
        super(message);
    }

    public ResourceNotFoundException(Throwable cause) {
        super(cause);
    }

}
